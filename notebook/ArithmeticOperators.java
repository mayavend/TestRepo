package notebook;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a=7;
        int b=2;
        int c=a/b;
        int d=a%b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(a++);
        System.out.println(a--);
    }
}
