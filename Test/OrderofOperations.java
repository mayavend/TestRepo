package Test;

public class OrderofOperations {
    public static void main(String[] args) {

        /* order of operations just like Math, Please Excuse My Dear Aunt Sally */
        int x = 5 + 3 * 2;
        System.out.println(x); // prints 11

        /* To change order of operations, use parenthesis */
        int y = (5 + 3) * 2;
        System.out.println(y); // prints 16
    }
}
